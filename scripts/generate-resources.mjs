#!/usr/bin/env node
/**
 * OpenAPI スキーマから CRUD リソース名を抽出し、
 * admin.frontend/src/generated/resources.g.ts を自動生成するスクリプト。
 *
 * CRUD リソースの判定:
 *   トップレベルパス (例: /admin-tool-users) に対応する
 *   パスパラメータ付きサブパス (例: /admin-tool-users/{id}) が存在すること。
 *
 * Usage: node scripts/generate-resources.mjs
 */
import { readFileSync, writeFileSync } from "fs";

const OPENAPI_PATH = "typespec/tsp-output/schema/openapi.admin.json";
const OUTPUT_PATH = "admin.frontend/src/generated/resources.g.ts";

const spec = JSON.parse(readFileSync(OPENAPI_PATH, "utf-8"));
const pathKeys = Object.keys(spec.paths ?? {});

// CRUD リソース判定: トップレベルパスに /{path}/{...} サブパスが存在する
const topLevel = pathKeys.filter((p) => /^\/[^/]+$/.test(p));
const crudResources = topLevel
  .map((p) => p.slice(1))
  .filter((name) => pathKeys.some((p) => p.startsWith(`/${name}/{`)));

/** kebab-case → camelCase */
function toCamelCase(str) {
  return str.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
}

const entries = crudResources
  .map(
    (name) =>
      `  ${toCamelCase(name)}: "${name}" as const satisfies ResourceName,`
  )
  .join("\n");

const content = `// Auto-generated by scripts/generate-resources.mjs — do not edit
import type { ResourceName } from "../types/resource";

export const resources = {
${entries}
} as const;
`;

writeFileSync(OUTPUT_PATH, content);
console.log(
  `✅ ${OUTPUT_PATH} を生成しました (${crudResources.length} リソース: ${crudResources.join(", ")})`
);

